package com.syntax.class03;

public class RealationalOperators {

	public static void main(String[] args) {
		int a = 10;
		int b = 15;
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a!=b);
		
		
		int x=100;
		int y=90;
		
		boolean result=x>y;
		
		System.out.println(result);
		
		result=x==y;
		System.out.println(result);
		
		byte num=10;
		byte num1=10;
		
		boolean boo=num>=num1;//false
		System.out.println(boo);

	}

}
