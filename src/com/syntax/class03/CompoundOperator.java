package com.syntax.class03;

public class CompoundOperator {

	public static void main(String[] args) {
		
		int num=100;
		
		num=num+10;
       System.out.println(num);
       
       num=num+200;
       
       System.out.println(num);
       
       //Compound 
       
       int myNumber=100;
       myNumber+=10;
       
       System.out.println(myNumber);
       
       myNumber+=200;
       
       System.out.println(myNumber);
       
       myNumber-=100;
       System.out.println(myNumber);
       
       myNumber/=10;
       System.out.println(myNumber);
       
       myNumber*=1000;
       System.out.println(myNumber);
       myNumber%=100;
       
       System.out.println(myNumber);
	}

}
